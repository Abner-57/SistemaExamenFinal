    public void insertarDatos (){
        try {
            String Query = "Insert into producto (codigoProducto,nombreProducto,precioUnitario,cantidadProducto,fechaVencimiento) values "
                    + "(?,?,?,?,?)";
            PreparedStatement ps = cn.con.prepareStatement(Query);
            ps.setString(1, txtcodigo.getText());
            ps.setString(2, textname.getText());
            ps.setString(3, textprecio.getText());
            ps.setString(4, txtcant.getText());
            ps.setString(5, txtfecha.getText());
            ps.execute();
            System.out.println("Datos Guardados correctamente.");
            JOptionPane.showMessageDialog(null, "Datos guardados correctamente.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudieron guardar los datos."+ e.getMessage());
            
        }
    }
    
