    public void cargarDatos(){
        String[] encabezado = {"Codigo","Producto","Precio","Cantidad","Fecha Ingreso"};
        String [] contRegistro = new String[10];
        DefaultTableModel modelo = new DefaultTableModel(null,encabezado);
        String Query = "select * from producto";
        try {
            Statement st = cn.con.createStatement();
            ResultSet rs = st.executeQuery(Query);
            while(rs.next()){
                contRegistro[0] = rs.getString("codigoProducto");
                contRegistro[1] = rs.getString("nombreProducto");
                contRegistro[2] = rs.getString("precioUnitario");
                contRegistro[3] = rs.getString("cantidadProducto");
                contRegistro[4] = rs.getString("fechaVencimiento");
                modelo.addRow(contRegistro);
            }
            tabla.setModel(modelo);
            
        } catch (Exception e) {
            System.out.println("ERROR, datos no cargados "+ e.getMessage());
            
            
        }
    }
    
    public void actualizarDatos(){
        try {
            String Query = "update producto set codigoProducto=?, nombreProducto=?, precioUnitario=?, cantidadProducto=?, fechaVencimiento=? where codigoProducto=?";
            
            PreparedStatement ps = cn.con.prepareStatement(Query);
            int rowSelect = tabla.getSelectedRow();
            String date = (String)tabla.getValueAt(rowSelect,0);
            ps.setString(1, txtcodigo.getText());
            ps.setString(2, textname.getText());
            ps.setString(3, textprecio.getText());
            ps.setString(4, txtcant.getText());
            ps.setString(5, txtfecha.getText());
            ps.setString(6,date);
            ps.execute();
            System.out.println("Datos actualizados");
            JOptionPane.showMessageDialog(null, "Datos actualizados correctamente.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR, no se actualizaron los datos."+ e.getMessage());
            
        }
    }
    
     public void eliminarDatos(){
        int rowSelect = tabla.getSelectedRow();
        try {
            String Query = "delete from producto where codigoProducto="+tabla.getValueAt(rowSelect, 0);
            Statement st = cn.con.createStatement();
            int num = st.executeUpdate(Query);
            if(num>=0){
                System.out.println("Se eliminaron los datos.");
                JOptionPane.showMessageDialog(null,"Datos eliminados correctamente");
            }
        } catch (Exception e) {
            System.out.println("ERROR, al eliminar los datos.");
                JOptionPane.showMessageDialog(null,"ERROR, no se pudieron eliminar."+e.getMessage());
        }
    }
    
